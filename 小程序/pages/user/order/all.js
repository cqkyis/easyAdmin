  /**
   * 生命周期函数--监听页面加载
   */
  onLoad: function (options) {
    var that = this;
    wx.setNavigationBarTitle({
      title: '全部订单',
    });
    var user = wx.getStorageSync('user');
    if(user){
     wx.request({
       url: orders.AllOrders,
       data:{
         uid:user.uid
       },
       success:function(res){
         console.log(res);
        that.setData({
         orderlist:res.data
        });
       }
     })
      

    }else{
       wx.navigateTo({
         url: '../login/login',
       })
    }
  },

